#include "minishell.h"

int	our_env(char **env)
{
	int	i;

	i = -1;
	if (env)
	{
		while (env[++i])
		{
			if (ft_strchr(env[i], '='))
				printf("%s\n", env[i]);
		}
	}
	return (0);
}
